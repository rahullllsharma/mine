import { describeArc, polarToCartesian } from "./math";

describe(polarToCartesian.name, () => {
  it.each([
    {
      params: [40, 50, 25, 25],
      expected: {
        x: 50.565456543517485,
        y: 27.342305324083753,
      },
    },

    {
      params: [60, 50, 75, 5],
      expected: {
        x: 66.53668070607436,
        y: -24.714602356880917,
      },
    },

    {
      params: [50, 50, 50, 50],
      expected: {
        x: 88.3022221559489,
        y: 17.860619515673037,
      },
    },

    {
      params: [50, 50, -50, 50],
      expected: {
        x: 11.697777844051096,
        y: 82.13938048432696,
      },
    },

    {
      params: [50, 50, -50, -50],
      expected: {
        x: 88.3022221559489,
        y: 82.13938048432698,
      },
    },

    {
      params: [50, -50, -50, -50],
      expected: {
        x: 88.3022221559489,
        y: -17.86061951567303,
      },
    },
    {
      params: [-50, -50, -50, -50],
      expected: {
        x: -11.697777844051103,
        y: -17.86061951567303,
      },
    },
  ])("should match the expected result", ({ params, expected }) => {
    // eslint-disable-next-line @typescript-eslint/ban-ts-comment
    // @ts-ignore
    expect(polarToCartesian(...params)).toMatchObject(expected);
  });
});

